#Tipo int e float
#Int -> Números inteiros
#O tipo int representa qualquer número
#positivo ou negativo. int sem sinal considerado
#positivo
print(11) #int
print(-11) #int
print(0)


#float -> Número com ponto flutuante
#O tipo float reprenta qualquer número
#positivo ou negativo com ponto flutuante
#float sem sinal é considerado positivo
print(1.1)
print(23.9)


#A funcao type mostra o tipo que o Python
#inferiu o valor
print(type('Breno'))
print(type(1.1), type('Nathalia'), type(10))

#Tipo de dado bool(boolean)
#Ao questionar algo em programa,
#só existem duas respostas possíveis
#sim (True) ou nao(False).
#Existem vários operadores para "questionar"
#Dentre eles o ==, que é um operador lógico que
#questiona se um valor é igual a outro

print(5 ==5)
print(1.1 == 1.1)
print(type(0 == 0))